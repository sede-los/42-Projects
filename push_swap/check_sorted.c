#include "push_swap.h"

int ft_check_sorted(t_stack *a)
{
    int i;

    i = a->nbr;
    while(a)
    {
        if(i > a->nbr)
            return(0);
        i = a->nbr;
        a = a->next;
    }
    return(1);
}
